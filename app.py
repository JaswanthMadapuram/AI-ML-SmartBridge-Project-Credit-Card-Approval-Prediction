from flask import Flask, render_template, request
import pandas as pd
import joblib

app = Flask(__name__)

# Load trained model and feature columns
model = joblib.load("credit_card_approval_model.pkl")
feature_columns = joblib.load("feature_columns.pkl")


@app.route("/")
def home():
    return render_template("index.html")


@app.route("/predict", methods=["POST"])
def predict():

    # Get values from HTML form
    gender = request.form["gender"]
    own_car = request.form["own_car"]
    own_house = request.form["own_house"]
    income = float(request.form["income"])
    children = int(request.form["children"])
    education = request.form["education"]
    occupation = request.form["occupation"]
    family_members = int(request.form["family_members"])

    # Create input dataframe
    input_df = pd.DataFrame({
        "CODE_GENDER": [gender],
        "FLAG_OWN_CAR": [own_car],
        "FLAG_OWN_REALTY": [own_house],
        "AMT_INCOME_TOTAL": [income],
        "CNT_CHILDREN": [children],
        "NAME_EDUCATION_TYPE": [education],
        "OCCUPATION_TYPE": [occupation],
        "CNT_FAM_MEMBERS": [family_members]
    })

    # Encode input exactly like training
    input_df = pd.get_dummies(input_df)

    # Add missing columns
    for col in feature_columns:
        if col not in input_df.columns:
            input_df[col] = 0

    # Keep same column order
    input_df = input_df[feature_columns]

    # Display the input going to the model
    print("\n========== INPUT TO MODEL ==========")
    print(input_df)

    # Prediction
    prediction = model.predict(input_df)[0]

    # Probability
    probability = model.predict_proba(input_df)[0]

    approval_probability = probability[0] * 100
    rejection_probability = probability[1] * 100

    print("\n========== MODEL OUTPUT ==========")
    print(f"Approval Probability : {approval_probability:.2f}%")
    print(f"Rejection Probability: {rejection_probability:.2f}%")

    if prediction == 0:
        print("Final Decision : APPROVED")
    else:
        print("Final Decision : REJECTED")

    if prediction == 0:
        
       result = f"""
            <h2 style="color:green;">✅ CREDIT CARD APPROVED</h2>
            <hr>
            <h3>Approval Probability : {approval_probability:.2f}%</h3>
            <h3>Rejection Probability : {rejection_probability:.2f}%</h3>
            <h3>Final Decision : APPROVED</h3>
            <h3>Risk Level : LOW RISK</h3>
            <h3>Reason</h3>
            <ul>
            <li>✔ Applicant profile indicates low credit risk.</li>
            <li>✔ Income and family details satisfy the approval criteria.</li>
            <li>✔ Rejection probability is below the decision threshold.</li>
            </ul>
            <hr>
            <b>Machine Learning Model :</b> Random Forest Classifier<br>
            <b>Dataset :</b> Credit Card Approval Dataset<br>
            <b>Prediction Type :</b> Binary Classification
            <hr>
            <p style="color:gray;">
            This prediction is generated by a Machine Learning model for educational purposes.
            </p>
            """
    else:
        
        result = f"""
            <h2 style="color:red;">❌ CREDIT CARD REJECTED</h2>
            <hr>
            <h3>Approval Probability : {approval_probability:.2f}%</h3>
            <h3>Rejection Probability : {rejection_probability:.2f}%</h3>
            <h3>Final Decision : REJECTED</h3>
            <h3>Risk Level : HIGH RISK</h3>
            <h3>Possible Reasons</h3>
            <ul>
                <li>✘ Applicant has a higher predicted credit risk.</li>
                <li>✘ The applicant profile is similar to previously rejected customers.</li>
                <li>✘ The model predicts higher financial risk.</li>
            </ul>
            <h3>Suggestions</h3>
            <ul>
                <li>✔ Increase annual income.</li>
                <li>✔ Improve repayment history.</li>
                <li>✔ Reduce outstanding debts.</li>
                <li>✔ Apply again after improving your financial profile.</li>
            </ul>
            <hr>
            <b>Machine Learning Model :</b> Random Forest Classifier<br>
            <b>Dataset :</b> Credit Card Approval Dataset<br>
            <b>Prediction Type :</b> Binary Classification
            <hr>
            <p style="color:gray;">
            This prediction is generated by a Machine Learning model for educational purposes.
            </p>
            """
        
    return render_template("index.html", prediction=result)

if __name__ == "__main__":
    app.run(debug=True)